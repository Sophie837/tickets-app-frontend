import { Events } from "./components/events";

const userId = 4
async function fetchData() {
  const response = await fetch(`http://localhost:8000/tickets/${userId}`, 
    { next: { tags: ['tickets'] }},
  )
  if (response.ok) {
    const tickets = await response.json();
    return tickets.data;
  }
}

